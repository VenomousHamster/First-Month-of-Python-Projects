tim = t.Turtle()
count = 1
y_axis = -300
tim.teleport(-200, -300)
tim.speed("fastest")
tim.hideturtle()
tim.penup()


for _ in range(0,100):
   tim.dot(18, random.choice(color_list))
   tim.forward(50)
   if count % 10 == 0:
       y_axis += 50
       tim.teleport(-200, y_axis)
   count += 1

screen = t.Screen()
